# Definir las variables
x = 3
y = 5

# Actualiza el valor de x
x = x + y  # x ahora es 8

# Actualiza el valor de y
y = 2 * x - y  # y ahora es 2*8 - 5 = 11

# Calcula el valor de x² + y²
resultado = x**2 + y**2  # 8**2 + 11**2 = 64 + 121 = 185

# Muestra los resultados
print("Valor final de x:", x)
print("Valor final de y:", y)
print("x² + y² =", resultado)
